function add(first, second) {
  return first + second
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function increment(a) {
  return a++
}

function decrement(a) {
  return a--
}

function makeInt(a) {
  return parseInt(a, 10)
}
